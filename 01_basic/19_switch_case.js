const month="march"
switch (month) {
    case "january":
        console.log("january")
        break;
    case "febuary":
        console.log("febuary")
        break;
    case "march":
        console.log("march")
        break;
    case "april":
        console.log("april")
        break;
    
    default:
        console.log("defult case match")
        break;
}